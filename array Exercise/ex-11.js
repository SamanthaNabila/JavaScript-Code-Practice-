//[5,2,9,1] → ascending sort

const arr = [5, 2, 9, 1];
arr.sort((a, b) => a - b);
console.log(arr);

// [5,2,9,1] → descending sort
const array = [5, 2, 9, 1];
array.sort((a, b) => b - a);
console.log(array);

// [1,2,3,4] → reverse করো
const arra = [1, 2, 3, 4, 1, 9, 7, 2];
arra.reverse();
console.log(arra);
